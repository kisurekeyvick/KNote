/**
 * vue 是一个渐进式框架
 * 
 * 渐进式：主张最少
 *          Angular强主张：必须使用它的模块机制- 必须使用它的依赖注入- 必须使用它的特殊形式定义组件
 *          React弱主张：函数式编程，知道什么是副作用，什么是纯函数
 *          Vue：没有强主张，你可以在原有大系统的上面，把一两个组件改用它实现，当jQuery用；也可以整个用它全家桶开发，当Angular用
 *              
 * 渐进式的含义，我的理解是：没有多做职责之外的事
 * 
 * DOM流存在缺陷的原因，在“DOM流”中其实是把dom当做Model，我们直接从dom中获取数据，随后又改变dom来更新视图，视图和模型其实是混在一起了，代码组织自然很混乱，不易维护。
 */

